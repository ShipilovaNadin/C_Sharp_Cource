// и возвращает значение этого элемента или же указание, что такого элемента нет.


void Print(int[,] arr)
{
    int row_size = arr.GetLength(0);
    int column_size = arr.GetLength(1);

    for (int i = 0; i < row_size; i++)
    {
        for (int j = 0; j < column_size; j++)
            Console.Write($" {arr[i, j],4} ");
        Console.WriteLine();
    }
    Console.WriteLine();

}

int[,] MassNums(int row, int column, int from, int to)
{
    int[,] arr = new int[row, column];

    for (int i = 0; i < row; i++)
        for (int j = 0; j < column; j++)
            arr[i, j] = new Random().Next(from, to);

    return arr;
}

void FindElement(int[,] arr)
{
    Console.WriteLine("Введите первую цифру позиции элемента: ");
    int i = int.Parse(Console.ReadLine());
    Console.WriteLine("Введите вторую цифру позиции элемента: ");
    int j = int.Parse(Console.ReadLine());
    Console.WriteLine();

    if (i <= arr.GetLength(0) && j <= arr.GetLength(1))
        Console.WriteLine($"{arr[i-1,j-1]}");
    else
        Console.WriteLine("Такого элемента нет");
}

